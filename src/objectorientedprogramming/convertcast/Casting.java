package objectorientedprogramming.convertcast;

public class Casting {

	public static void main(String[] args) {
		double d = 5.5;
		int i = 8;
		float f = 435634634;
		int j = (int) (d * i * f);
		System.out.println(j);
		Double dd = 10.5555;
		String s = String.valueOf(dd);
		System.out.println(s);
	
	}
		
}
